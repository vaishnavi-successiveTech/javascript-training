// Write a program to dispaly number from 1-20 using all types of loops
console.log("for loop is used.");
// using for loop
for(let i=1;i<21;i++){
    console.log(i);
}
console.log("while loop is used.");
// using while loop
let num=1;
while(num<21){
    console.log(num);
    num++;
}

// using do-while loop
console.log("do-while loop is used.");
let number=1;
do {
    console.log(number);
    number++;

}while(number<=20);
